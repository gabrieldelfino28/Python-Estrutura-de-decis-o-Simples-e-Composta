print("Insera um valor para determinar o dia da semana")
dias = input("1 para Domingo, 2 para Segunda, 3 para Terça, 4 para Quarta,\n5 para Quinta, 6 para Sexta e 7 para Sabádo: ")

if (dias == "1") :
    print("\nDomingo")
    
elif (dias == "2") :
    print("\nSegunda-Feira")

elif (dias == "3") :
    print("\nTerça-Feira")

elif (dias == "4") :
    print("\nQuarta-Feira")

elif (dias == "5") :
    print("\nQuinta-Feira")

elif (dias == "6") :
    print("\nSexta-Feira")

elif (dias == "7") :
    print("\nSabádo")

else :
    print ("\nValor Inválido")
